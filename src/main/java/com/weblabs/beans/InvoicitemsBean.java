package com.weblabs.beans;

public class InvoicitemsBean {

	
	private String invoiceitemsID;
	private String serviceitem;
	private String description;
	private String unitcost;
	private String qty;
	private String amount;
	private String invoiceid;
	
	
	public String getInvoiceitemsID() {
		return invoiceitemsID;
	}
	public void setInvoiceitemsID(String invoiceitemsID) {
		this.invoiceitemsID = invoiceitemsID;
	}
	public String getServiceitem() {
		return serviceitem;
	}
	public void setServiceitem(String serviceitem) {
		this.serviceitem = serviceitem;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getUnitcost() {
		return unitcost;
	}
	public void setUnitcost(String unitcost) {
		this.unitcost = unitcost;
	}
	public String getQty() {
		return qty;
	}
	public void setQty(String qty) {
		this.qty = qty;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getInvoiceid() {
		return invoiceid;
	}
	public void setInvoiceid(String invoiceid) {
		this.invoiceid = invoiceid;
	}
	
}
